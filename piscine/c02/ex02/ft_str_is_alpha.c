#include <string.h>

int	ft_str_is_alpha(char *str)
{
	char	j;
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		j = str[i];
		if (!((j >= 'a' && j <= 'z') || (j >= 'A' && j <= 'Z')))
			return (0);
		i++;
	}
	return (1);
}
